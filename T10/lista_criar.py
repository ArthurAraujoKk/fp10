# Criando uma lista de alunos
alunos = ["Ana", "Bruno", "Carla", "Daniel"]

# Exibindo a lista completa
print(f"Lista de alunos: {alunos}")

alunos[0] = "Alice"

# Acessando o primeiro elemento (indice 0)
print(f"Primeiro aluno: {alunos[0]}")

print(f"Ultimo aluno: {alunos[-1]}")

# Verificando quantos elementos tem na lista
print(f"Quantidade de alunos: {len(alunos)}")